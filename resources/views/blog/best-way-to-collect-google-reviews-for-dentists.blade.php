        <h3>2. Train Your Front Desk Team</h3>

        <p>Your front desk staff interact with every patient at checkout — the perfect moment for a casual, friendly review request. Train them to say something like:</p>

        <blockquote>
            <p>"Glad everything went well today! You'll get a quick email from us in a bit — if you could leave us a Google review, we'd really appreciate it. It helps other patients find us."</p>
        </blockquote>

        <p>This works because it sets an expectation. When the email arrives later, the patient already knows what it is and is more likely to follow through. The verbal ask primes the pump; the email makes it easy to act.</p>

        <h3>3. Use QR Codes in Your Office</h3>

        <p>Place QR codes that link directly to your Google review page throughout your practice:</p>

        <ul>
            <li><strong>Reception desk</strong> — a small stand with "Love your smile? Leave us a review!" and a QR code</li>
            <li><strong>Checkout counter</strong> — patients often wait a moment while payment processes</li>
            <li><strong>Hygiene rooms</strong> — a framed card patients see while waiting for the dentist</li>
            <li><strong>Post-procedure care cards</strong> — include a QR code alongside aftercare instructions</li>
            <li><strong>Appointment reminder cards</strong> — patients take these home and see them again later</li>
        </ul>

        <p>QR codes work particularly well in dental offices because patients often have idle moments — waiting for the hygienist, sitting in the chair before the doctor arrives, or standing at checkout. Give them something productive to do with that time.</p>

        <h3>4. Follow Up After Major Procedures</h3>

        <p>For significant work like implants, crowns, root canals, or orthodontic treatment, send a follow-up message a few days after the procedure. Check on their recovery first, then gently ask for a review:</p>

        <blockquote>
            <p>Hi [First Name], just checking in to see how you're feeling after your [procedure] on [day]. If everything is going well and you have a moment, we'd love a quick Google review about your experience. Here's the link: [link]</p>
        </blockquote>

        <p>This approach shows you care about outcomes, not just collecting reviews. Patients notice that — and it makes them more willing to write something thoughtful.</p>

        <h3>5. Leverage Your Hygienists</h3>

        <p>Dental hygienists often spend more one-on-one time with patients than the dentist does. They build rapport during cleanings and are often the person patients feel most comfortable with. A casual mention from a hygienist carries real weight:</p>

        <blockquote>
            <p>"Your teeth look great today! If you've been happy with your care here, a Google review would really help us out. We'll send you an easy link after your visit."</p>
        </blockquote>

        <p>The key is making it feel natural, not scripted. Hygienists should mention reviews conversationally, not read from a prompt card.</p>

        <h3>6. Respond to Every Single Review</h3>

        <p>This isn't just a collection strategy — it's a multiplier. When patients see that you personally respond to reviews, two things happen:</p>

        <p>First, patients who are on the fence about leaving a review are more likely to do so when they see the dentist actually reads and responds to them. It feels like their words matter.</p>

        <p>Second, your responses are visible to every potential patient reading your reviews. A warm, personal reply to a positive review reinforces your practice's personality. A professional, empathetic response to a negative review demonstrates maturity and accountability.</p>

        <p>Keep responses genuine. Thank the patient by name, reference something specific about their visit if possible, and keep it brief. Avoid copy-pasting the same response to every review — patients can tell.</p>

        <h3>7. Create a "Review Us" Page on Your Website</h3>

        <p>Add a simple page on your dental practice website with a direct link to your Google review page. Include it in your main navigation or footer. This gives patients an easy way to find your review link anytime — not just when they receive an email.</p>

        <p>You can also add the link to your email signature, appointment confirmation emails, and patient newsletters. Every touchpoint is an opportunity.</p>

        <h2>Common Mistakes Dental Practices Make</h2>

        <ul>
            <li><strong>Only asking happy-looking patients.</strong> You can't always predict who will leave a great review. Some of your best reviews will come from patients who seemed quiet or neutral during their visit. Ask everyone consistently.</li>
            <li><strong>Offering whitening or discounts for reviews.</strong> This violates Google's terms of service. If Google detects incentivized reviews, they can remove all your reviews and suspend your listing. The risk isn't worth it.</li>
            <li><strong>Ignoring negative reviews.</strong> A single unaddressed negative review can outweigh ten positive ones. Always respond promptly, professionally, and with an offer to resolve the issue offline. Remember HIPAA — never discuss treatment details in a public response.</li>
            <li><strong>Asking at the wrong time.</strong> Requesting a review while a patient is still numb, in pain, or processing bad news about their oral health will not end well. Read the room.</li>
            <li><strong>Not having a system.</strong> Relying on individual staff members to remember to ask is not a strategy. It's a hope. Automate the process so every patient gets asked, every time.</li>
        </ul>

        <h2>Building a Review System for Your Dental Practice</h2>

        <p>The practices that dominate Google reviews in their area all share one thing in common: <strong>a repeatable system</strong>. Here's what that looks like for a dental office:</p>

        <ol>
            <li><strong>Patient completes their appointment</strong> and checks out at the front desk</li>
            <li><strong>Front desk gives a verbal nudge</strong> — "You'll get a quick email from us, we'd love a review if you have a sec"</li>
            <li><strong>Automated review request fires</strong> within 1-2 hours via email</li>
            <li><strong>Smart routing directs the response</strong> — satisfied patients go to Google, dissatisfied patients go to a private feedback form</li>
            <li><strong>One gentle reminder</strong> goes out after 3 days if no response</li>
            <li><strong>Doctor or office manager responds</strong> to every new Google review within 24-48 hours</li>
        </ol>

        <p>When this runs consistently, you'll see a steady stream of new reviews every week. Over time, that compounds into a review profile that no competitor can match overnight.</p>

