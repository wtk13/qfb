        <h2>Mistakes That Kill Your Response Rate</h2>

        <p>Even a good template won't save you from these common errors:</p>

        <ul>
            <li><strong>Sending from a no-reply address.</strong> Customers are less likely to engage with an email they can't respond to. Use a real email address — it signals that there's a real person behind the request.</li>
            <li><strong>Burying the link.</strong> The review link or button should be impossible to miss. Put it front and center, not at the bottom after three paragraphs of text.</li>
            <li><strong>Asking for reviews on multiple platforms at once.</strong> "Leave us a review on Google, Yelp, or Facebook!" gives the customer a decision to make instead of an action to take. Pick one platform — Google should be your default — and make that the only ask.</li>
            <li><strong>Offering incentives.</strong> "Leave a review and get 10% off your next visit" violates Google's terms of service and can get your reviews removed or your listing penalized. Don't risk it.</li>
            <li><strong>Waiting too long.</strong> An email sent two weeks after the service might as well not be sent at all. The customer has moved on. Automate your emails to send within hours, not days.</li>
            <li><strong>Not sending any follow-up.</strong> A single email gets a response rate of roughly 5-10%. Adding one well-timed follow-up can nearly double that. Don't leave those reviews on the table.</li>
        </ul>

        <h2>How to Set Up Automated Review Request Emails</h2>

        <p>Manually sending emails after every job is not sustainable. You'll forget, you'll get busy, and your review growth will stall. The businesses that consistently collect reviews are the ones that automate the process.</p>

        <p>Here's what a good automated system looks like:</p>

        <ol>
            <li><strong>Customer completes a service or purchase</strong></li>
            <li><strong>Your system sends a review request email</strong> within 1-2 hours automatically</li>
            <li><strong>The customer clicks and rates their experience</strong> — happy customers are sent to Google, unhappy customers are routed to a private feedback form</li>
            <li><strong>If no response, a single follow-up email</strong> is sent 3 days later</li>
            <li><strong>You respond to every new Google review</strong> within 24-48 hours</li>
        </ol>

        <p>This system runs in the background. You don't have to think about it, manage it, or remind your team. Every customer gets asked. Every positive experience has a chance to become a public review. And every negative experience gets caught privately before it goes public.</p>

        <p>The difference between a business with 30 reviews and a business with 300 reviews is rarely the quality of service — it's whether they have a system in place to ask consistently.</p>

