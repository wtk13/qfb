        <h1>Review Request Email Template for Small Business (Copy & Paste)</h1>

        <p>You finished the job. The customer is happy. They said "thank you" and meant it. But a week later, your Google listing still has the same number of reviews it had last month. Sound familiar?</p>

        <p>The gap between a satisfied customer and a published review is almost never about willingness. Most happy customers would leave a review — they just need to be asked, and they need it to be easy. That's where a well-written <strong>review request email</strong> comes in.</p>

        <p>In this guide, you'll find ready-to-use email templates designed for small businesses, along with the principles that make them work. Copy them, customize them, and start turning happy customers into public proof.</p>

        <h2>Why Email Is Still the Best Channel for Review Requests</h2>

        <p>Text messages feel intrusive. Phone calls are awkward. Social media is noisy. Email sits in a sweet spot: it's professional, non-pushy, and gives the customer space to act on their own time.</p>

        <p>For small businesses, email also has practical advantages:</p>

        <ul>
            <li><strong>You already have the address</strong> — from invoices, booking confirmations, or intake forms. No extra data collection needed.</li>
            <li><strong>It's easy to automate</strong> — set it up once and every customer gets asked, every time, without relying on your memory or your team's.</li>
            <li><strong>It provides a direct link</strong> — you can embed a button that takes the customer straight to your Google review page. No searching, no friction.</li>
            <li><strong>It's trackable</strong> — you can see who opened, who clicked, and who followed through, which helps you refine your approach over time.</li>
        </ul>

        <h2>What Makes a Review Request Email Actually Work</h2>

        <p>Before you copy any template, understand the four principles that separate emails that get reviews from emails that get ignored:</p>

        <h3>1. Keep It Short</h3>

        <p>Your customer is not going to read three paragraphs about why reviews matter to your business. They'll scan the email in five seconds, decide if they care, and either click or delete. Your email should be scannable in that window. Aim for 50-75 words in the body — anything longer and your conversion rate drops.</p>

        <h3>2. Make the Ask Specific</h3>

        <p>Don't say "we'd love your feedback." Say "would you leave us a quick Google review?" Vague requests get vague results. When the customer knows exactly what you're asking and exactly where to do it, they're far more likely to follow through.</p>

        <h3>3. Reduce Friction to Zero</h3>

        <p>Every extra step you add between "I should leave a review" and "done" loses you a percentage of respondents. Include a direct link or button to your Google review page. Don't make them search for your business. Don't send them to your website first. One click, one destination.</p>

        <h3>4. Send It at the Right Time</h3>

        <p>The best time to send a review request email is within 1-24 hours of completing the service. The experience is still vivid, the positive feelings are fresh, and the customer hasn't moved on to the next thing in their life. Wait a week and you've lost most of them.</p>

        <h2>5 Review Request Email Templates You Can Use Today</h2>

        <h3>Template 1: The Simple Ask</h3>

        <p>Best for: any small business after a completed service or purchase.</p>

        <blockquote>
            <p><strong>Subject: How did we do, [First Name]?</strong></p>
            <p>Hi [First Name],</p>
            <p>Thanks for choosing [Business Name]. We hope you had a great experience!</p>
            <p>If you have 30 seconds, a quick Google review would mean the world to us. It helps other people in [city/area] find us.</p>
            <p>[Leave a Review button]</p>
            <p>Thank you!</p>
            <p>[Your Name], [Business Name]</p>
        </blockquote>

        <p><strong>Why it works:</strong> it's short, specific, and tells the customer exactly what to do. The "30 seconds" framing makes it feel low-effort. Mentioning the local area adds a community element.</p>

        <h3>Template 2: The Personal Touch</h3>

        <p>Best for: service businesses where you worked directly with the customer (contractors, consultants, personal trainers, etc.).</p>

        <blockquote>
            <p><strong>Subject: It was great working with you, [First Name]</strong></p>
            <p>Hi [First Name],</p>
            <p>I really enjoyed working on [project/service] with you. I'm glad we could [specific result — e.g., "get your kitchen looking brand new" or "sort out those back issues"].</p>
            <p>If you're happy with the results, would you mind sharing your experience in a quick Google review? It's the best way to help us reach more people like you.</p>
            <p>[Leave a Review button]</p>
            <p>Thanks again,</p>
            <p>[Your Name]</p>
        </blockquote>

        <p><strong>Why it works:</strong> referencing the specific work you did makes the email feel personal, not automated. Customers are more likely to respond to something that feels like it was written for them.</p>

        <h3>Template 3: The Follow-Up Nudge</h3>

        <p>Best for: a gentle reminder 3-5 days after your first email went unopened or unanswered.</p>

        <blockquote>
            <p><strong>Subject: Quick favor, [First Name]?</strong></p>
            <p>Hi [First Name],</p>
            <p>I know life gets busy, so I'll keep this short. If you had a good experience with [Business Name], a Google review would really help us out.</p>
            <p>It only takes a minute, and it makes a big difference for a small business like ours.</p>
            <p>[Leave a Review button]</p>
            <p>Appreciate it!</p>
            <p>[Your Name]</p>
        </blockquote>

        <p><strong>Why it works:</strong> it acknowledges that the customer is busy, keeps the ask light, and the "small business like ours" framing taps into the natural desire to support local businesses. One follow-up is fine. Two starts to feel pushy — stick to one reminder maximum.</p>

        <h3>Template 4: After a Repeat Visit</h3>

        <p>Best for: businesses with returning customers — salons, gyms, restaurants, clinics, auto shops.</p>

        <blockquote>
            <p><strong>Subject: Thanks for coming back, [First Name]!</strong></p>
            <p>Hi [First Name],</p>
            <p>We love seeing you again! Since you've been coming to [Business Name] for a while, your opinion really carries weight.</p>
            <p>Would you take a moment to share what keeps you coming back? A Google review from a regular customer like you means more than you'd think.</p>
            <p>[Leave a Review button]</p>
            <p>See you next time!</p>
            <p>[Your Name / Team Name]</p>
        </blockquote>

        <p><strong>Why it works:</strong> repeat customers have stronger opinions and more credibility. This template validates their loyalty and turns it into motivation. Their reviews also tend to be more detailed and persuasive because they can speak to consistency over time.</p>

        <h3>Template 5: After Resolving a Problem</h3>

        <p>Best for: when a customer had an issue that your team resolved successfully.</p>

        <blockquote>
            <p><strong>Subject: Glad we could make it right, [First Name]</strong></p>
            <p>Hi [First Name],</p>
            <p>I'm glad we were able to sort things out for you. Your satisfaction genuinely matters to us, and I'm happy we could get it right.</p>
            <p>If you feel we handled the situation well, we'd appreciate a Google review sharing your experience. It helps other customers know they can trust us — even when things don't go perfectly the first time.</p>
            <p>[Leave a Review button]</p>
            <p>Thanks for giving us the chance to fix it.</p>
            <p>[Your Name]</p>
        </blockquote>

        <p><strong>Why it works:</strong> reviews that mention problem resolution are incredibly powerful. They show potential customers that your business takes accountability and follows through. Use this template carefully — only send it once you're confident the customer is genuinely satisfied with the outcome.</p>

